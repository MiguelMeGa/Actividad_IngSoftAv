package paquete;

public class calculadora {

	public calculadora()
	{
		
		
	}
	
	////////////////////////////////////////
	//             Método de la suma
	////////////////////////////////////////
	
	public double suma (double a, double b)
	{
		
		return a+b;//Se devuelve la suma de a +b
	}
	
	//////////////////////////////////////////
	// 			Método para la resta
	/////////////////////////////////////////
	
	public double resta(double a, double b)
	{
		return a-b;
	}
	
	
	
}
